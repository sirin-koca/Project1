# We declare and assign values to several variables of different data types, including str, int, float, and bool.
# We also show the rules for naming variables in Python, such as that variable names must start with
# a letter or an underscore, can't start with a number, can contain letters, numbers, and underscores, and must not
# be the same as any Python keywords.

# Variable declaration and assignment
name = "John Doe"
age = 30

# Variables can be of different data types
is_active = True
gpa = 3.7

# Variables names must start with a letter or an underscore
_private = 10

# Variables can't start with a number
# 1_num = 20 # This will raise a SyntaxError

# Variables can't contain spaces
# full name = "Jane Doe" # This will raise a SyntaxError

# Variables can contain letters, numbers and underscores
roll_no = 123_456

# Variables are case-sensitive
full_name = "Jane Doe"
Full_Name = "John Doe"

# Variable names can't be the same as Python keywords
# for = 10 # This will raise a SyntaxError

print(name)
print(age)
print(is_active)
print(gpa)
print(_private)
print(roll_no)
print(full_name)
print(Full_Name)


